package org.example;

public class Coordinate {

    private int x;
    private double y;

    public Coordinate(int x, double y)
    {
        this.x = x;
        this.y = y;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }
}
